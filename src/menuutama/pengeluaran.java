    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        String kode = txkoo.getText();
        Date tanggal = tanggals.getDate();
        String ket = txket.getText();
        String nominal = txnominal.getText();

        try {
            Connection c = koneksi.getKoneksi();
            String sql = "INSERT INTO pengeluaran (kode_pengeluaran, tanggal, keterangan, nominal) VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, kode);

            SimpleDateFormat outputFormat = new SimpleDateFormat("dd-MM-yyyy");
            String tanggalString = outputFormat.format(tanggal);

            Date parsedDate = outputFormat.parse(tanggalString);

            p.setDate(2, new java.sql.Date(parsedDate.getTime()));

            p.setString(3, ket);
            p.setString(4, nominal);

            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        bersih();
        loadtable();
        autonumber();
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        btnsimpan.setEnabled(true);

    if (txkoo.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Pilih Dulu Data Yang Akan Diedit!");
    } else {
        try {
            // Formatter untuk tanggal dalam format ddMMyyyy
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate = dateFormat.format(tanggals.getDate());

            String sql = "UPDATE pengeluaran SET tanggal = ?, keterangan = ?, nominal = ? WHERE kode_pengeluaran = ?";
            java.sql.Connection conn = (java.sql.Connection) koneksi.getKoneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, formattedDate); // Set the formatted date
            pst.setString(2, txket.getText());
            pst.setString(3, txnominal.getText());
            pst.setString(4, txkoo.getText());
            
            pst.execute();
            bersih();
            loadtable();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    }//GEN-LAST:event_btneditActionPerformed

