    private void autonumber() {
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM pengeluaran ORDER BY kode_pengeluaran DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String kode = r.getString("kode_pengeluaran").substring(3);
                String TR = "" + (Integer.parseInt(kode) + 1);
                String Nol = "";

                if (TR.length() == 1) {
                    Nol = "000";
                } else if (TR.length() == 2) {
                    Nol = "00";
                } else if (TR.length() == 3) {
                    Nol = "0";
                } else if (TR.length() == 3) {
                    Nol = "";
                }
                txkoo.setText("KLR" + Nol + TR);
            } else {
                txkoo.setText("KLR0001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
